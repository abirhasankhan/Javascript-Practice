// while loop

let index = 0;
while ( index <= 10 ) {

    console.log(`Value of index is ${index}`);
    index = index + 2;
}

let myArray = ['flash', 'batman', 'superman'];

let arr = 0;

while ( arr < myArray.length ) {

    console.log(`Value of array is ${myArray[arr]}`);

    arr++;
}


// do while loop

let score = 1;

do {

    console.log(`score is ${score}`);
    score++;
    
} while (score <= 10);

